using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MyTestProject
{
    class Circle
    {
        public double per(double a)
        {
            return 2 * Math.PI * a;
        }

        public double Area(double a)
        {
            return Math.PI * Math.Pow(a, 2);
        }
    }
